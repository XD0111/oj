#include<bits/stdc++.h>
using namespace std;

int main()
{
 	int x;
 	while(cin>>x)
 	{
 		switch(x)
 		{
 		case 1:puts("monday");break;
 		case 2:puts("tuesday");break;
 		case 3:puts("wednesday");break;
 		case 4:puts("thursday");break;
 		case 5:puts("friday");break;
 		case 6:puts("saturday");break;
 		case 7:puts("sunday");break;
 		}
	}
 	return 0;
}
